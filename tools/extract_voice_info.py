SOUND_DIR = "../sound"

def ScanTracksForVoice(voicegroup, voice_index):
    tracks_path = sorted(Path(f"{SOUND_DIR}/tracks").iterdir(), key=lambda x: int(x.stem.split('_')[1]))

    for track in tracks_path:
        if not track.is_file():
            continue

        voicegroup_found = False
        voice_index_found = False
        with track.open() as f:
            for line in f:
                if voicegroup.strip(".inc") in line:
                    voicegroup_found = True
                voice = line.strip().split(", ")
                if (f".byte VOICE" in voice[0]) and (voice_index == int(voice[1])):
                    voice_index_found = True
            if (voicegroup_found is True) and (voice_index_found is True):
                track_number = int(track.name.removeprefix("track_").removesuffix(".s"))
                print(f"        Found in track: {track.name} aka {Tracks[track_number]}")

    return

def ScanVoiceGroup(voicegroup_file: Path):
    voice_index = 0
    skip_beginning = 0
    with voicegroup_file.open() as f:
        for line in f:
            if skip_beginning != 5:
                skip_beginning += 1
                continue

            if "DirectSoundData" not in line:
                voice_index += 1
                continue

            split_line = line.strip().split(", ")
            for item in split_line:
                if "DirectSoundData" in item:
                    print(f"    Current voice: {item}, Index: {voice_index}")
                    ScanTracksForVoice(voicegroup_file.name, voice_index)
                    voice_index += 1

    return

def GoThroughVoiceGroups():
    voicegroups_path = sorted(Path(f"{SOUND_DIR}/voicegroups").iterdir(), key=lambda x: x.name)

    for voicegroup in voicegroups_path:
        if voicegroup.is_file():
            # print(f"File: {item.name}, Path: {item}")
            print(f"Current voicegroup: {voicegroup.name}")
            ScanVoiceGroup(voicegroup)
    
    return
            
GoThroughVoiceGroups()
